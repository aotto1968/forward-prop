void ki_xform_shuffle_apply(uint8_t *restrict out,
                             const uint8_t *restrict in,
                             int n, int ch) {
    for (int pl = 0; pl < ch; pl++) {
        const uint8_t *s = in + (size_t)pl * (size_t)n;
        uint8_t *d = out + (size_t)pl * (size_t)n;
        for (int i = 0; i < n; i++)
            d[i] = s[ki_xform_shuffle_map[i]];
    }
}

