/* ── GZIP decompression ────────────────────────────────────────── */
static int ki_decompress_gz(const char *path, uint8_t **out_data, size_t *out_size) {
    FILE *f = fopen(path, "rb");
    if (!f) return -1;
    fseek(f, 0, SEEK_END);
    long gz_len = ftell(f);
    fseek(f, 0, SEEK_SET);

    uint8_t *gz_buf = (uint8_t *)malloc((size_t)gz_len);
    if (!gz_buf) { fclose(f); return -1; }
    if (fread(gz_buf, 1, (size_t)gz_len, f) != (size_t)gz_len) {
        free(gz_buf); fclose(f); return -1;
    }
    fclose(f);

    z_stream strm;
    memset(&strm, 0, sizeof(strm));
    strm.avail_in = (uInt)gz_len;
    strm.next_in = gz_buf;

    if (inflateInit2(&strm, 16 + MAX_WBITS) != Z_OK) {
        free(gz_buf); return -1;
    }

    size_t buf_cap = 65536;
    size_t buf_used = 0;
    uint8_t *buf = (uint8_t *)malloc(buf_cap);

    do {
        if (buf_used + 65536 > buf_cap) {
            buf_cap *= 2;
            uint8_t *newbuf = (uint8_t *)realloc(buf, buf_cap);
            if (!newbuf) { inflateEnd(&strm); free(gz_buf); free(buf); return -1; }
            buf = newbuf;
        }
        strm.avail_out = (uInt)(buf_cap - buf_used);
        strm.next_out = buf + buf_used;
        int ret = inflate(&strm, Z_NO_FLUSH);
        buf_used = buf_cap - strm.avail_out;
        if (ret == Z_STREAM_END) break;
        if (ret != Z_OK) {
            inflateEnd(&strm); free(gz_buf); free(buf); return -1;
        }
    } while (strm.avail_out == 0);

    inflateEnd(&strm);
    free(gz_buf);
    *out_data = buf;
    *out_size = buf_used;
    return 0;
}

/* ── Fashion-MNIST read ──────────────────────────────────────────── */
static int ki_fashion_read(ki_FashionData *out) {
    const char *candidates[] = {
        "data/mnist-fashion",
        "../data/mnist-fashion",
        "www/data/mnist-fashion",        /* from fashion/ subdir */
        "../www/data/mnist-fashion",     /* from project root */
        NULL
    };
    const char *data_dir = NULL;
    for (int i = 0; candidates[i]; i++) {
        char test[512];
        snprintf(test, sizeof(test), "%s/train-images-idx3-ubyte.gz", candidates[i]);
        if (access(test, R_OK) == 0) {
            data_dir = candidates[i];
            break;
        }
    }
    if (!data_dir) {
        fprintf(stderr, "[FATAL] Cannot find Fashion-MNIST data.\n");
        return -1;
    }

    int dry_run = out->dry_run;
    memset(out, 0, sizeof(*out));
    out->dry_run = dry_run;
    uint8_t *raw = NULL;
    size_t raw_size = 0;
    char path[512];

    /* Read images */
    snprintf(path, sizeof(path), "%s/train-images-idx3-ubyte.gz", data_dir);
    if (ki_decompress_gz(path, &raw, &raw_size) != 0) return -1;

    int magic    = (raw[0] << 24) | (raw[1] << 16) | (raw[2] << 8) | raw[3];
    int num_img  = (raw[4] << 24) | (raw[5] << 16) | (raw[6] << 8) | raw[7];
    int rows     = (raw[8] << 24) | (raw[9] << 16) | (raw[10] << 8) | raw[11];
    int cols     = (raw[12] << 24) | (raw[13] << 16) | (raw[14] << 8) | raw[15];
    int pixels   = rows * cols;

    printf("  [FASHION] images: magic=0x%08X %d x %d px=%d\n",
           magic, rows, cols, pixels);

    if (magic != 0x00000803) {
        fprintf(stderr, "[FATAL] Not Fashion-MNIST image file (magic=0x%08X)\n", magic);
        free(raw); return -1;
    }

    out->num_images = num_img;
    out->n_train = num_img;         /* all loaded images for training */
    out->n_eval = 10000;            /* test set (t10k) */
    out->rows = rows;
    out->cols = cols;
    out->pixels = pixels;

    /* Dry-run: header already parsed, skip pixel data */
    if (dry_run) {
        free(raw);
        printf("  [FASHION] dry-run: %d images, %d px each\n", num_img, pixels);
        return 0;
    }

    size_t npix = (size_t)num_img * (size_t)pixels;
    out->X_raw = (uint8_t *)malloc(npix);
    memcpy(out->X_raw, raw + 16, npix);
    out->X = (float *)malloc(npix * sizeof(float));
    for (size_t i = 0; i < npix; i++)
        out->X[i] = ((float)raw[16 + i] / 255.0f) * 2.0f - 1.0f;
    free(raw);

    /* Read labels */
    snprintf(path, sizeof(path), "%s/train-labels-idx1-ubyte.gz", data_dir);
    if (ki_decompress_gz(path, &raw, &raw_size) != 0) { free(out->X); return -1; }

    int lbl_magic = (raw[0] << 24) | (raw[1] << 16) | (raw[2] << 8) | raw[3];
    int lbl_count = (raw[4] << 24) | (raw[5] << 16) | (raw[6] << 8) | raw[7];

    printf("  [FASHION] labels: magic=0x%08X count=%d\n", lbl_magic, lbl_count);

    if (lbl_magic != 0x00000801) {
        fprintf(stderr, "[FATAL] Not Fashion-MNIST label file (magic=0x%08X)\n", lbl_magic);
        free(raw); free(out->X); return -1;
    }

    out->y = (uint8_t *)malloc((size_t)lbl_count);
    memcpy(out->y, raw + 8, (size_t)lbl_count);
    free(raw);

    /* ── Load test data (t10k) ─────────────────────────────── */
    char tepath[512];
    snprintf(tepath, sizeof(tepath), "%s/t10k-images-idx3-ubyte.gz", data_dir);
    int has_test = (access(tepath, R_OK) == 0);
    if (has_test) {
        uint8_t *te_raw = NULL; size_t te_sz = 0;
        if (ki_decompress_gz(tepath, &te_raw, &te_sz) == 0) {
            int te_num = (te_raw[4]<<24)|(te_raw[5]<<16)|(te_raw[6]<<8)|te_raw[7];
            int te_magic = (te_raw[0]<<24)|(te_raw[1]<<16)|(te_raw[2]<<8)|te_raw[3];
            if (te_magic == 0x00000803 && te_num > 0) {
                size_t te_npix = (size_t)te_num * (size_t)pixels;
                size_t old_npix = (size_t)num_img * (size_t)pixels;
                /* Load test labels */
                snprintf(tepath, sizeof(tepath), "%s/t10k-labels-idx1-ubyte.gz", data_dir);
                uint8_t *tel = NULL; size_t tlsz = 0;
                int lab_ok = (ki_decompress_gz(tepath, &tel, &tlsz) == 0);
                int te_lbl_cnt = 0;
                if (lab_ok) {
                    int lm = (tel[0]<<24)|(tel[1]<<16)|(tel[2]<<8)|tel[3];
                    te_lbl_cnt = (tel[4]<<24)|(tel[5]<<16)|(tel[6]<<8)|tel[7];
                    lab_ok = (lm == 0x00000801 && te_lbl_cnt == te_num);
                }
                if (lab_ok) {
                    /* Realloc + copy images */
                    out->X_raw = (uint8_t *)realloc(out->X_raw, old_npix + te_npix);
                    memcpy(out->X_raw + old_npix, te_raw + 16, te_npix);
                    out->X = (float *)realloc(out->X, (old_npix + te_npix) * sizeof(float));
                    for (size_t i = 0; i < te_npix; i++)
                        out->X[old_npix + i] = ((float)te_raw[16 + i] / 255.0f) * 2.0f - 1.0f;
                    /* Realloc + copy labels */
                    out->y = (uint8_t *)realloc(out->y, (size_t)num_img + (size_t)te_num);
                    memcpy(out->y + num_img, tel + 8, (size_t)te_num);
                    out->num_images += te_num;
                    out->n_eval = te_num;
                }
                if (tel) free(tel);
            }
            free(te_raw);
        }
    }

    printf("  [FASHION] Loaded %d samples (%d px)\n", out->num_images, out->pixels);
    return 0;
}

static inline void ki_fashion_free(ki_FashionData *data) {
    free(data->X);
    free(data->X_raw);
    free(data->y);
    memset(data, 0, sizeof(*data));
}

/* ── PNG writer (dataset-specific: grayscale for Fashion-MNIST) ── */
__attribute__((unused))
static void _png_be32(FILE *f, uint32_t v) {
    uint8_t buf[4] = {
        (uint8_t)((v >> 24) & 0xFF),
        (uint8_t)((v >> 16) & 0xFF),
        (uint8_t)((v >>  8) & 0xFF),
        (uint8_t)( v        & 0xFF)
    };
    fwrite(buf, 1, 4, f);
}

__attribute__((unused))
static void _png_chunk(FILE *f, const char type[4],
                        const void *data, size_t len) {
    unsigned long crc = crc32(0L, NULL, 0);
    _png_be32(f, (uint32_t)len);
    crc = crc32(crc, (const unsigned char *)type, 4);
    fwrite(type, 1, 4, f);
    if (len > 0 && data) {
        crc = crc32(crc, (const unsigned char *)data, (unsigned int)len);
        fwrite(data, 1, len, f);
    }
    _png_be32(f, (uint32_t)(crc & 0xFFFFFFFFUL));
}

__attribute__((unused))
static void ki_write_png(const char *path,
                          const uint8_t *gray,
                          const uint8_t *plane1, const uint8_t *plane2,
                          int w, int h) {
    (void)plane1; (void)plane2;
    FILE *f = fopen(path, "wb");
    if (!f) { fprintf(stderr, "[ERROR] Cannot write %s\n", path); return; }

    uint8_t sig[8] = {137,80,78,71,13,10,26,10};
    fwrite(sig, 1, 8, f);

    uint8_t ihdr[13];
    for (int i = 0; i < 4; i++) {
        ihdr[3-i] = (uint8_t)((uint32_t)w >> (i*8));
        ihdr[7-i] = (uint8_t)((uint32_t)h >> (i*8));
    }
    ihdr[8]  = 8;  ihdr[9]  = 0;  ihdr[10] = 0;  ihdr[11] = 0;  ihdr[12] = 0;
    _png_chunk(f, "IHDR", ihdr, 13);

    size_t row_bytes = (size_t)w;
    size_t raw_size = (size_t)h * (1 + row_bytes);
    uint8_t *raw = (uint8_t *)malloc(raw_size);
    for (int y = 0; y < h; y++) {
        size_t y_off = (size_t)y * (1 + row_bytes);
        raw[y_off] = 0;
        memcpy(raw + y_off + 1, gray + (size_t)y * (size_t)w, row_bytes);
    }

    uLongf comp_len = compressBound(raw_size);
    uint8_t *comp = (uint8_t *)malloc(comp_len);
    if (compress(comp, &comp_len, raw, raw_size) != Z_OK) {
        free(raw); free(comp); fclose(f); return;
    }
    _png_chunk(f, "IDAT", comp, comp_len);
    _png_chunk(f, "IEND", NULL, 0);

    fclose(f);
    free(raw);
    free(comp);
}

